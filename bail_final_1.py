def calc_cat_emb_dim(num_categories:int):
    return min(50, max(4, int(round(math.sqrt(num_categories)*2))))

class HybridLegalModel(nn.Module):
    def __init__(self):
        super().__init__()
        self.bert = AutoModel.from_pretrained(MODEL_NAME)
        self.cat_embeds = nn.ModuleList([nn.Embedding(cat_vocab[col]["num_categories_with_unk"], calc_cat_emb_dim(cat_vocab[col]["num_categories_with_unk"])) for col in CATEGORICAL_COLS])
        self.num_proj = nn.Linear(len(NUMERIC_COLS),16) if NUMERIC_COLS else None
        concat_dim = self.bert.config.hidden_size + sum([e.embedding_dim for e in self.cat_embeds]) + (16 if NUMERIC_COLS else 0)
        self.classifier = nn.Sequential(
    nn.Dropout(DROPOUT),
    nn.Linear(concat_dim, 256),
    nn.GELU(),                # ← adds non-linearity
    nn.Dropout(DROPOUT),
    nn.Linear(256, 2)
)


    def forward(self, input_ids, attention_mask, cat=None, num=None):
        out = self.bert(input_ids=input_ids, attention_mask=attention_mask)
        text_vec = out.pooler_output if hasattr(out,"pooler_output") and out.pooler_output is not None else (out.last_hidden_state*attention_mask.unsqueeze(-1)).sum(1)/attention_mask.unsqueeze(-1).sum(1).clamp(min=1e-6)
        cat_vec = torch.cat([emb(cat[:,i]) for i,emb in enumerate(self.cat_embeds)], dim=1) if cat is not None and cat.size(1)>0 else torch.zeros((input_ids.size(0),0), device=input_ids.device)
        num_vec = self.num_proj(num) if num is not None and num.size(1)>0 else torch.zeros((input_ids.size(0),0), device=input_ids.device)
        combo = torch.cat([text_vec, cat_vec, num_vec], dim=1)
        return self.classifier(combo)

