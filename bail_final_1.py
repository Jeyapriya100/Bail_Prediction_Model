class BailDataset(Dataset):
    def __init__(self, df):
        self.df = df.reset_index(drop=True)
        self.texts = df["text"].tolist()
        self.labels = torch.tensor(df[LABEL_COL].tolist(), dtype=torch.long)
        self.cat_data = torch.tensor([
            [cat_vocab[col]["stoi"].get(row[col], cat_vocab[col]["num_categories"]) for col in CATEGORICAL_COLS]
            for _, row in df.iterrows()
        ], dtype=torch.long) if CATEGORICAL_COLS else torch.zeros((len(df),0),dtype=torch.long)
        self.num_data = torch.tensor(num_scaler.transform(df[NUMERIC_COLS]) if NUMERIC_COLS else np.zeros((len(df),0)), dtype=torch.float32)

    def __len__(self): return len(self.df)

    def __getitem__(self, idx):
        enc = tokenizer(self.texts[idx], padding="max_length", truncation=True, max_length=MAX_LENGTH, return_tensors="pt")
        return {
            "input_ids": enc["input_ids"].squeeze(0),
            "attention_mask": enc["attention_mask"].squeeze(0),
            "labels": self.labels[idx],
            "cat": self.cat_data[idx],
            "num": self.num_data[idx]
        }

def collate_fn(batch):
    input_ids = torch.stack([b["input_ids"] for b in batch])
    attention_mask = torch.stack([b["attention_mask"] for b in batch])
    labels = torch.stack([b["labels"] for b in batch])
    cat = torch.stack([b["cat"] for b in batch])
    num = torch.stack([b["num"] for b in batch])
    return {"input_ids": input_ids, "attention_mask": attention_mask, "labels": labels, "cat": cat, "num": num}

