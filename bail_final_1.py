def fairness_analysis(model, dataloader, attribute, groups):
    """
    Computes accuracy and macro-F1 for each group.
    attribute: str, categorical column name in your dataset (e.g., 'accused_gender')
    groups: list of group names to evaluate
    """
    model.eval()
    results = []

    # Iterate over groups
    for group in groups:
        y_true, y_pred = [], []

        for batch_idx, batch in enumerate(dataloader):
            # Extract original categorical values from dataset
            indices = batch_idx * dataloader.batch_size + torch.arange(len(batch["labels"]))
            batch_group_vals = batch["cat"][:, CATEGORICAL_COLS.index(attribute)]  # embedding index
            # Map back to string using vocab
            batch_group_strs = [cat_vocab[attribute]["itos"].get(int(val), "Unknown") for val in batch_group_vals]
            mask = [i for i, val in enumerate(batch_group_strs) if val == group]

            if len(mask) == 0:
                continue

            inputs = batch["input_ids"][mask].to(DEVICE)
            attn = batch["attention_mask"][mask].to(DEVICE)
            labels = batch["labels"][mask].cpu().numpy()

            logits = model(inputs, attn,
                           cat=batch["cat"][mask].to(DEVICE),
                           num=batch["num"][mask].to(DEVICE))
            preds = torch.argmax(logits, dim=1).cpu().numpy()

            y_true.extend(labels)
            y_pred.extend(preds)

        if y_true:
            acc = accuracy_score(y_true, y_pred)
            f1 = f1_score(y_true, y_pred, average="macro")
            results.append({"Group": group, "Accuracy": acc, "F1": f1})

    return pd.DataFrame(results)

# -------------------------
# Case Prioritization
# -------------------------
def case_prioritization(model, dataloader, top_k=10):
    """
    Returns both high-confidence and low-confidence cases.
    - High-confidence: top_k with highest probability.
    - Low-confidence: top_k with probabilities closest to 0.5 (most ambiguous).
    """
    model.eval()
    records = []

    with torch.no_grad():
        for batch_idx, batch in enumerate(dataloader):
            inputs = batch["input_ids"].to(DEVICE)
            attn = batch["attention_mask"].to(DEVICE)
            logits = model(inputs, attn,
                           cat=batch["cat"].to(DEVICE),
                           num=batch["num"].to(DEVICE))
            probs = torch.softmax(logits, dim=1)
            preds = torch.argmax(probs, dim=1)

            confidence = probs.max(dim=1).values.cpu().numpy()
            uncertainty = np.abs(probs[:,1].cpu().numpy() - 0.5)  # distance from 0.5

            batch_indices = batch_idx * dataloader.batch_size + np.arange(len(batch["labels"]))

            for idx, pred, conf, prob, unc in zip(batch_indices, preds.cpu().numpy(),
                                                  confidence, probs.cpu().numpy(), uncertainty):
                records.append({
                    "case_index": idx,
                    "pred_label": int(pred),
                    "confidence": float(conf),
                    "uncertainty": float(unc),
                    "prob_rejected": float(prob[0]),
                    "prob_granted": float(prob[1])
                })

    df_records = pd.DataFrame(records)

    # High-confidence → sort descending
    high_conf = df_records.sort_values("confidence", ascending=False).head(top_k)

    # Low-confidence → sort by lowest uncertainty (closest to 0.5)
    low_conf = df_records.sort_values("uncertainty", ascending=True).head(top_k)

    return high_conf, low_conf

