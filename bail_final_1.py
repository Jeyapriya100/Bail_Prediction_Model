def counterfactual_gender_check(case, model, tokenizer, device="cpu"):
    """
    Run a counterfactual test by swapping the accused_gender
    (Male ↔ Female) and checking prediction changes.
    """
    results = {}

    for gender in ["Male", "Female"]:
        case_copy = case.copy()
        case_copy["accused_gender"] = gender

        # --- Preprocess text ---
        text = case_copy.get("facts", "") + " " + case_copy.get("judgment_reason", "")
        enc = tokenizer(
            text,
            truncation=True,
            padding="max_length",
            max_length=256,
            return_tensors="pt"
        ).to(device)

        # --- Encode categorical into a list (same order as training) ---
        cat_values = [
            cat_vocab["court"]["stoi"].get(case_copy["court"], 0),
            cat_vocab["crime_type"]["stoi"].get(case_copy["crime_type"], 0),
            cat_vocab["accused_gender"]["stoi"].get(case_copy["accused_gender"], 0),
            cat_vocab["region"]["stoi"].get(case_copy["region"], 0),
            cat_vocab["prior_cases"]["stoi"].get(str(case_copy["prior_cases"]), 0)
        ]
        cat_input = torch.tensor([cat_values], dtype=torch.long, device=device)

        # --- No numeric features anymore ---
        numeric_input = torch.zeros((1, 0), dtype=torch.float32, device=device)

        # --- Run model ---
        model.eval()
        with torch.no_grad():
            logits = model(
                input_ids=enc["input_ids"],
                attention_mask=enc["attention_mask"],
                cat=cat_input,
                num=numeric_input
            )
            probs = torch.softmax(logits, dim=1).cpu().numpy()[0]

        results[gender] = {
            "prediction": "Granted" if np.argmax(probs) == 1 else "Rejected",
            "prob_rejected": float(probs[0]),
            "prob_granted": float(probs[1])
        }

    # ---- Compare fairness ----
    comparison = {
        "stable": results["Male"]["prediction"] == results["Female"]["prediction"],
        "male_pred": results["Male"]["prediction"],
        "female_pred": results["Female"]["prediction"]
    }

    return results, comparison

